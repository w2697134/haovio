// 关键词打分匹配,返回最佳条目;无命中返回 null
export function matchKb(query: string): KbEntry | null {
  const q = query.toLowerCase().trim();
  if (!q) return null;
  let best: KbEntry | null = null;
  let bestScore = 0;
  for (const entry of KB) {
    let score = 0;
    for (const kw of entry.keywords) {
      if (q.includes(kw.toLowerCase())) score += kw.length; // 长词权重更高
    }
    if (score > bestScore) {
      bestScore = score;
      best = entry;
    }
  }
  return bestScore > 0 ? best : null;
}
